<?php


/**
 * Funcion que retorna la suma de dos numeros
 *
 * @param int $n1
 * @param int $n2
 * @return int
 */

function suma($n1, $n2) {  
     return $n1+$n2;
}

$resultado = suma(5, 26);
echo $resultado;
echo "\n";


